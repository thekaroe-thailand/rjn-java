class Operator {
    public static void main(String[] args) {
        int a = 36;
        int b = 10;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b); // คูณ
        System.out.println(a / b); // หาร
        System.out.println(a % b);
    }
}
