public class Array2D {
    public static void main(String[] args) {
        int[][] numbers = {
                { 1, 2, 3 }, // index 0
                { 4, 5, 6 }, // index 1
                { 7, 8, 9 }, // index 2
                { 10, 11, 12 } // index 3
        };

        System.out.println(numbers[0][2]);
        System.out.println(numbers[4][0]);
    }
}
